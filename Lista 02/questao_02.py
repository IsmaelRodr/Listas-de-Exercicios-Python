# Você viu na televisão que esse inverno vai ser rigoroso no Brasil. Decidiu então que vai monitorar a 
# temperatura da sua cidade. Se as temperaturas estiverem abaixo de 17ºC, você vai exibir na tela: “Frio da 
# moléstia", mas caso contrário: “Tudo normal nas terras de Cuçumarim”.
# Resposta:

temperatura = 17

is_frio = temperatura < 17

